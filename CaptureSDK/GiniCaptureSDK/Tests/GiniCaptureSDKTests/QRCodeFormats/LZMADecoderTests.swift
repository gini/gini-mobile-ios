    @Test func decodesLongDistanceMatchCorrectly() {
        let expected = Self.expectedPlaintext()
        let decoded = LZMADecoder.decode(input: Self.lzmaLongDistanceStream,
                                         outputLength: expected.count)
        #expect(decoded == expected)
    }

    @Test func decodedSecondBlockMatchesFirst() {
        // The bug corrupted only the copied (matched) region; assert both halves
        // are byte-identical so a future regression can't pass on the literals alone.
        let expected = Self.expectedPlaintext()
        let decoded = LZMADecoder.decode(input: Self.lzmaLongDistanceStream,
                                         outputLength: expected.count)
        #expect(decoded?.prefix(256) == decoded?.suffix(256))
    }
}
