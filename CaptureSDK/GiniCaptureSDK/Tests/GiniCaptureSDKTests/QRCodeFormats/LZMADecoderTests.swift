@Suite("LZMA Decoder")
struct LZMADecoderTests {

    // Plaintext = a 256-byte block repeated twice. The second block is a match at
    // distance 256, which forces posSlot >= 14 and therefore exercises
    // `decodeDirectBits`. Regression guard for the bit-inversion bug that silently
    // corrupted any decode with a back-reference distance >= 128 bytes.
    private static func expectedPlaintext() -> [UInt8] {
        let block = (0..<256).map { UInt8(($0 * 7 + 3) & 0xFF) }
        return block + block
    }

